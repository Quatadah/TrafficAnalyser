const getEntryExitData = (date: string) => {
    return data.filter((item) => item.date === date);
};

const getEntries = (date: string) => {
    return getEntryExitData(date).filter((item) => item.sens === "Entrée");
};

const getExits = (date: string) => {
    return getEntryExitData(date).filter((item) => item.sens === "Sortie");
};

export interface EntryExit {
    type: string;
    entry: number;
    exit: number;
}

export const getEntryExitByDate = (date: string) => {
    const entries = getEntries(date);
    const exits = getExits(date);
    const entryExit: EntryExit[] = [];
    entries.forEach((entry) => {
        const exit = exits.find((exit) => exit.type === entry.type);
        if (entry.type !== "VL") {
            entryExit.push({
                type: entry.type,
                entry: entry.number,
                exit: exit ? exit.number : 0,
            });
        }
    });

    return entryExit;
};
